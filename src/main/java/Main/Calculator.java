package Main;

class Calculator {

    int add(int x, int y) {
        return x + y;
    }

    int subtract(int x, int y) {
        return x - y;
    }

    int multiply(int x, int y) {
        return x * y;
    }

    double divide(double x, double y) {
        return x / y;
    }

    int modulus(int x, int y) {
        return x % y;
    }

}
